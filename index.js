    function toggleLanguage() {
        currentLang = currentLang === 'sq' ? 'en' : 'sq';
        document.getElementById('langBtn').textContent = currentLang === 'sq' ? 'EN' : 'SQ';
        updateLanguage();
    }
    
    function updateLanguage() {
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translations[currentLang][key]) {
                element.textContent = translations[currentLang][key];
            }
        });
    }
    
    function showNotification(message, type = 'success') {
        const notification = document.getElementById('notification');
        document.getElementById('notificationText').textContent = message;
        
        notification.className = `notification ${type === 'success' ? 'bg-green-500' : 'bg-red-500'} text-white px-6 py-3 rounded-lg shadow-lg show`;
        
        setTimeout(() => {
            notification.classList.remove('show');
        }, 5000); 
    }
    
