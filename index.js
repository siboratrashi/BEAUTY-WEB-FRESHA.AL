    function filterByCategory(category) {
        // Remove active class from all buttons
        document.querySelectorAll('.category-filter').forEach(button => {
            button.classList.remove('active', 'bg-pink-500', 'text-white');
            button.classList.add('border-gray-400', 'text-gray-700');
        });
        
        // Add active class to clicked button
        event.target.classList.add('active', 'bg-pink-500', 'text-white');
        event.target.classList.remove('border-gray-400', 'text-gray-700');
    
        console.log('Filtering by category:', category);

        showNotification(`Po shfaqen shërbimet për kategorinë: ${category}`);
    }
    
