    function filterByCategory(category) {
        // Merr të gjitha kartat e salloneve
        const cards = document.querySelectorAll('.salon-card');

        cards.forEach(card => {
            const cardCategory = card.getAttribute('data-category');

            if (category === 'all' || cardCategory === category) {
                card.style.display = 'block'; // shfaqe
            } else {
                card.style.display = 'none'; // fshije
            }
        });

        // Ndrysho butonin aktiv
        document.querySelectorAll('.category-filter').forEach(btn => {
            btn.classList.remove('active', 'bg-pink-500', 'text-white');
            btn.classList.add('border-gray-300', 'text-gray-700');
        });

        const activeBtn = document.querySelector(`.category-filter[onclick="filterByCategory('${category}')"]`);
        if (activeBtn) {
            activeBtn.classList.add('active', 'bg-pink-500', 'text-white');
            activeBtn.classList.remove('text-gray-700');
        }
    }

